// try to find my libemail script
$libemail='none';

// look in home

$home = getenv("HOME");
if ((!$home) || ($home == '')) {
    if (is_dir('/home/logicuma')) {
        $home = '/home/logicuma';
    } else {
        if (is_dir('/home/kck')) {
            $home = '/home/kck/';
        }
    }
}

if (file_exists("$home/bin/kckmail/libemail.php")) {
    $libemail = "$home/bin/kckmail/libemail.php";
}

// look up several subfolders
if ($libemail === "none") {
    $trynext = dirname(___FILE___) . '/../../../bin/kckmail/libemail.php';
    if (file_exists($trynext)) {
        $libemail = realpath ($trynext);
    }
}

